def calculate_average(num1, num2, num3):
    """
    Calculates the mathematical average of three numbers.
    """
    total = num1 + num2 + num3
    return total / 3

def add_tax(bill_total):
    """
    Returns the total bill including 10% tax.
    """
    return bill_total * 1.10

def greet_user(name):
    """
    Returns a greeting string.
    """
    return "Hello " + name